    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int codigo = Integer.parseInt(this.CodigoABuscar.getText());
        int index = Buscar(codigo);
        if (index>0) {
        JOptionPane.showMessageDialog(null,ListaEmpleados.get(index));    
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.nombre = this.jTextField1.getText();
        this.codigo = Integer.parseInt(this.jTextField2.getText());
        this.cargo = this.jTextField3.getText();
        
        buttonGroup1.add(Hsi);
        buttonGroup1.add(Hno);
        
        if (Hsi.isSelected()) {
            this.hijos = "Si";
        }else if(Hno.isSelected()){
            this.hijos = "No";
        }else{
            this.hijos = "Sin marcar";
        }
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        buttonGroup1.clearSelection();
        empleado = new Empleado(nombre, codigo, hijos, cargo);
        ListaEmpleados.add(empleado);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        Object[] fila = {empleado.getNombre(), empleado.getCodigo(),empleado.getHijos(), empleado.getCargo()};
        modelo.addRow(fila);
    }//GEN-LAST:event_jButton1ActionPerformed

