    public int Buscar(int codigo){
        for (Empleado ListaEmpleado : ListaEmpleados) {
            if (ListaEmpleado.getCodigo() == codigo) {
                ListaEmpleados.indexOf(ListaEmpleado);
            }
        }
        return -1;
    }
    
    
